// Function that takes a number and returns "Even" or "Odd"
function checkEvenOdd(number) {
    if (number % 2 === 0) {
      return "Even";
    } else {
      return "Odd";
    }
  }
  
  // Example usage
  console.log(checkEvenOdd(87)); // Output: "Odd"
  console.log(checkEvenOdd(8)); // Output: "Even"
  
  // Loop that prints numbers from 1 to 10
  for (let i = 1; i <= 10; i++) {
    console.log(i);
  }